package com.samton.web.manage.task.bean.vo;

import cn.afterturn.easypoi.excel.annotation.Excel;

import java.math.BigDecimal;
import java.util.Date;

public class CustTaskExcelVo {
    @Excel(name = "订单编号")
    private String orderCode;
    @Excel(name = "买家账号代码")
    private String buyerCode;
    @Excel(name = "订单数量")
    private Integer orderNum;
    @Excel(name = "税收($)")
    private BigDecimal tax;
    @Excel(name = "执行日期", format = "yyyy/MM/dd")
    private Date execDate;
    @Excel(name = "应充金额($)")
    private BigDecimal depositMoney;
    @Excel(name = "任务负责人")
    private String liableUserName;
    @Excel(name = "备注")
    private String remark;

    public String getOrderCode() {
        return orderCode;
    }

    public void setOrderCode(String orderCode) {
        this.orderCode = orderCode;
    }

    public String getBuyerCode() {
        return buyerCode;
    }

    public void setBuyerCode(String buyerCode) {
        this.buyerCode = buyerCode;
    }

    public Integer getOrderNum() {
        return orderNum;
    }

    public void setOrderNum(Integer orderNum) {
        this.orderNum = orderNum;
    }

    public BigDecimal getTax() {
        return tax;
    }

    public void setTax(BigDecimal tax) {
        this.tax = tax;
    }

    public Date getExecDate() {
        return execDate;
    }

    public void setExecDate(Date execDate) {
        this.execDate = execDate;
    }

    public BigDecimal getDepositMoney() {
        return depositMoney;
    }

    public void setDepositMoney(BigDecimal depositMoney) {
        this.depositMoney = depositMoney;
    }

    public String getLiableUserName() {
        return liableUserName;
    }

    public void setLiableUserName(String liableUserName) {
        this.liableUserName = liableUserName;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }
}
